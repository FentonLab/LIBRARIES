#ifndef SIZE_TYPES_IN
#define SIZE_TYPES_IN

/* machine dependent "c" types */

typedef	unsigned char	U_I_1;	/* one byte unsigned integer */
typedef	char		S_I_1;	/* one byte signed integer */
typedef unsigned short	U_I_2;	/* two byte unsigned integer */
typedef short		S_I_2;	/* two byte signed integer */
typedef unsigned int	U_I_4;	/* four byte unsigned integer */ 
typedef int		S_I_4;	/* four byte signed integer */ 
typedef float		S_F_4;	/* four byte signed floating point number */ 
typedef double		S_F_8;	/* eight byte signed floating point number */ 

#endif
